package es.paqueteria.paqueteria.modelo;

public class Peso {
	
	private Integer idPeso;
	private double peso;
	
	public Peso(Integer idPeso, double peso) {
		this.idPeso = idPeso;
		this.peso = peso;
	}

	public Integer getIdPeso() {
		return idPeso;
	}

	public double getPeso() {
		return peso;
	}
	
	
}
